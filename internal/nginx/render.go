type renderedSite struct {
	ID                  string
	DomainList          string
	HealthBody          string
	PrimaryHostPort     string
	PrimaryTLSName      string
	PrimaryScheme       string
	UseTLS              bool
	BackupHostPort      string
	BackupTLSName       string
	BackupHostHeader    string
	HostHeader          string
	CacheGeneration     int64
	GRPC                bool
	Passthrough         bool
	ClientMaxBodySizeMB int
	ReadWriteTimeout    string
	CacheEnabled        bool
	AlwaysUnbuffered    bool
}

type renderInput struct {
	Sites                        []renderedSite
	DefaultHTTP                  bool
	SecurityEnabled              bool
	SecurityConfig               string
	RateLimitEnabled             bool
	RateLimitConfig              string
	CachePath                    string
	CacheMaxSize                 string
	ClientKeepaliveTimeout       string
	ClientKeepaliveRequests      int
	UpstreamConnectTimeout       string
	UpstreamKeepaliveConnections int
	StaticAssetPathPattern       string
}

func Render(sites []domain.Site) (string, error) {
	return RenderWithSecurityAndRateLimit(sites, nil, nil)
}

func RenderWithSecurity(sites []domain.Site, securityPolicies []domain.SecurityPolicy) (string, error) {
	return RenderWithSecurityAndRateLimit(sites, securityPolicies, nil)
}

func RenderWithSecurityAndRateLimit(sites []domain.Site, securityPolicies []domain.SecurityPolicy, rateLimitPolicies []domain.RateLimitPolicy) (string, error) {
	var err error
	if rateLimitPolicies != nil {
		rateLimitPolicies, err = normalizeRateLimitPolicies(rateLimitPolicies)
		if err != nil {
			return "", err
		}
	}
	items := make([]renderedSite, 0, len(sites))
	dedicatedTCP := false
	for _, site := range sites {
		if site.TCPOnly {
			dedicatedTCP = true
		}
		if !site.Enabled || site.TCPOnly {
			continue
		}
		clientMaxBodySizeMB, err := domain.NormalizeClientMaxBodySizeMB(site.ClientMaxBodySizeMB)
		if err != nil {
			return "", fmt.Errorf("site %s: %w", site.Name, err)
		}
		readWriteTimeoutSeconds, err := domain.NormalizeReadWriteTimeoutSeconds(site.ReadWriteTimeoutSeconds)
		if err != nil {
			return "", fmt.Errorf("site %s: %w", site.Name, err)
		}
		primary, err := parseOrigin(site.PrimaryOrigin)
		if err != nil {
			return "", fmt.Errorf("site %s primary origin: %w", site.Name, err)
		}
		if site.Passthrough && primary.Scheme != "http" && primary.Scheme != "https" {
			return "", fmt.Errorf("site %s passthrough mode is only supported for HTTP and HTTPS origins", site.Name)
		}
		item := renderedSite{
			ID: site.ID, DomainList: strings.Join(site.Domains, " "), HealthBody: SiteHealthBody(site.ID), PrimaryHostPort: primary.Host, PrimaryTLSName: site.PrimaryOrigin.TLSServerName,
			PrimaryScheme: domain.ProxyScheme(primary.Scheme), UseTLS: domain.OriginUsesTLS(primary.Scheme), HostHeader: site.PrimaryOrigin.HostHeader, CacheGeneration: site.CacheGeneration,
			GRPC: domain.IsGRPCScheme(primary.Scheme), Passthrough: site.Passthrough, ClientMaxBodySizeMB: clientMaxBodySizeMB,
			ReadWriteTimeout: fmt.Sprintf("%ds", readWriteTimeoutSeconds),
			CacheEnabled:     primary.Scheme == "http" || primary.Scheme == "https",
			AlwaysUnbuffered: site.Passthrough || domain.IsWebSocketScheme(primary.Scheme),
		}
		item.CacheEnabled = item.CacheEnabled && !site.Passthrough
		if item.HostHeader == "" {
			item.HostHeader = primary.Hostname()
		}
		if item.PrimaryTLSName == "" {
			item.PrimaryTLSName = primary.Hostname()
		}
		if site.BackupOrigin != nil && site.BackupOrigin.Enabled {
			backup, err := parseOrigin(*site.BackupOrigin)
			if err != nil {
				return "", fmt.Errorf("site %s backup origin: %w", site.Name, err)
			}
			if backup.Scheme != primary.Scheme {
				return "", fmt.Errorf("site %s origins must use the same scheme", site.Name)
			}
			item.BackupHostPort = backup.Host
			item.BackupTLSName = site.BackupOrigin.TLSServerName
			item.BackupHostHeader = site.BackupOrigin.HostHeader
			if item.BackupHostHeader == "" {
				item.BackupHostHeader = backup.Hostname()
			}
			if item.BackupTLSName == "" {
				item.BackupTLSName = backup.Hostname()
			}
		}
		items = append(items, item)
	}
	sort.Slice(items, func(i, j int) bool { return items[i].ID < items[j].ID })
	template, err := template.New("nginx").Parse(configTemplate)
	if err != nil {
		return "", err
	}
	var out bytes.Buffer
	if err := template.Execute(&out, renderInput{
		Sites:                        items,
		DefaultHTTP:                  !dedicatedTCP,
		SecurityEnabled:              len(enabledSecurityPolicies(securityPolicies)) > 0 && !dedicatedTCP,
		SecurityConfig:               renderSecurityConfig(securityPolicies, !dedicatedTCP),
		RateLimitEnabled:             len(enabledRateLimitPolicies(rateLimitPolicies)) > 0 && !dedicatedTCP,
		RateLimitConfig:              renderRateLimitConfig(rateLimitPolicies, !dedicatedTCP),
		CachePath:                    DefaultCachePath,
		CacheMaxSize:                 fmt.Sprintf("%dg", DefaultCacheMaxBytes/(1<<30)),
		ClientKeepaliveTimeout:       defaultClientKeepaliveTimeout,
		ClientKeepaliveRequests:      defaultClientKeepaliveRequests,
		UpstreamConnectTimeout:       defaultUpstreamConnectTimeout,
		UpstreamKeepaliveConnections: defaultUpstreamKeepaliveConnections,
		StaticAssetPathPattern:       staticAssetPathPattern,
	}); err != nil {
		return "", err
	}
	return out.String(), nil
}

func SplitHTTPConfig(configuration string) (string, []domain.NginxConfigFragment, error) {
	const markerPrefix = "# CDN HTTP site fragment "
	var base strings.Builder
	fragments := make([]domain.NginxConfigFragment, 0)
	cursor := 0
	for {
		relativeStart := strings.Index(configuration[cursor:], markerPrefix)
		if relativeStart < 0 {
			base.WriteString(configuration[cursor:])
			break
		}
		start := cursor + relativeStart
		lineEnd := strings.IndexByte(configuration[start:], '\n')
		if lineEnd < 0 {
			return "", nil, errors.New("HTTP site fragment marker is incomplete")
		}
		lineEnd += start
		marker := strings.TrimSpace(configuration[start:lineEnd])
		if !strings.HasSuffix(marker, " begin") {
			return "", nil, fmt.Errorf("invalid HTTP site fragment marker %q", marker)
		}
		siteID := strings.TrimSuffix(strings.TrimPrefix(marker, markerPrefix), " begin")
		if siteID == "" {
			return "", nil, errors.New("HTTP site fragment marker is missing a site ID")
		}
		endMarker := markerPrefix + siteID + " end"
		relativeEnd := strings.Index(configuration[lineEnd+1:], endMarker)
		if relativeEnd < 0 {
			return "", nil, fmt.Errorf("HTTP site fragment %s has no end marker", siteID)
		}
		end := lineEnd + 1 + relativeEnd + len(endMarker)
		if end < len(configuration) && configuration[end] == '\n' {
			end++
		}
		base.WriteString(configuration[cursor:start])
		fragments = append(fragments, domain.NginxConfigFragment{Name: siteFragmentName(siteID), Content: configuration[start:end]})
		cursor = end
	}
	return base.String(), fragments, nil
}

func SplitConfigFragments(httpConfiguration, streamConfiguration string) (*domain.NginxConfigFragments, error) {
	httpBase, httpSites, err := SplitHTTPConfig(httpConfiguration)
	if err != nil {
		return nil, err
	}
	streamBase, streamSites, err := SplitStreamConfig(streamConfiguration)
	if err != nil {
		return nil, err
	}
	return &domain.NginxConfigFragments{
		HTTPBase: httpBase, HTTPSites: httpSites, StreamBase: streamBase, StreamSites: streamSites,
	}, nil
}

func siteFragmentName(siteID string) string {
	var name strings.Builder
	for _, character := range siteID {
		if character >= 'a' && character <= 'z' || character >= 'A' && character <= 'Z' ||
			character >= '0' && character <= '9' || character == '-' || character == '_' || character == '.' {
			name.WriteRune(character)
			continue
		}
		name.WriteByte('-')
	}
	return "site-" + name.String() + ".conf"
}

func normalizeRateLimitPolicies(policies []domain.RateLimitPolicy) ([]domain.RateLimitPolicy, error) {
	result := make([]domain.RateLimitPolicy, 0, len(policies))
	for _, policy := range policies {
		normalized, err := domain.NormalizeRateLimitPolicy(policy)
		if err != nil {
			return nil, fmt.Errorf("rate limit policy %q: %w", policy.Name, err)
		}
		result = append(result, normalized)
	}
	return result, nil
}

func enabledRateLimitPolicies(policies []domain.RateLimitPolicy) []domain.RateLimitPolicy {
	result := make([]domain.RateLimitPolicy, 0, len(policies))
	for _, policy := range policies {
		if policy.Enabled {
			result = append(result, policy)
		}
	}
	sort.Slice(result, func(i, j int) bool { return result[i].ID < result[j].ID })
	return result
}

func enabledSecurityPolicies(policies []domain.SecurityPolicy) []domain.SecurityPolicy {
	result := make([]domain.SecurityPolicy, 0, len(policies))
	for _, policy := range policies {
		if policy.Enabled {
			result = append(result, policy)
		}
	}
	sort.Slice(result, func(i, j int) bool {
		if result[i].Priority != result[j].Priority {
			return result[i].Priority < result[j].Priority
		}
		return result[i].ID < result[j].ID
	})
	return result
}

func renderSecurityConfig(policies []domain.SecurityPolicy, httpEnabled bool) string {
	if policies == nil {
		return ""
	}
	var result strings.Builder
	result.WriteString(SecurityRevisionMarker(policies))
	result.WriteByte('\n')
	policies = enabledSecurityPolicies(policies)
	if len(policies) == 0 || !httpEnabled {
		return result.String()
	}
	result.WriteString("map $uri $cdn_security_policy_id {\n    default \"\";\n")
	for _, policy := range policies {
		result.WriteString("    \"~")
		result.WriteString(escapeNginxQuotedPattern(policy.Pattern))
		result.WriteString("\" \"")
		result.WriteString(policy.ID)
		result.WriteString("\";\n")
	}
	result.WriteString("}\n\nmap $cdn_security_policy_id $cdn_security_action {\n    default \"\";\n")
	for _, policy := range policies {
		result.WriteString(fmt.Sprintf("    \"%s\" \"%s\";\n", policy.ID, policy.Action))
	}
	result.WriteString("}\n\nmap $cdn_security_policy_id $cdn_security_ban_seconds {\n    default 0;\n")
	for _, policy := range policies {
		result.WriteString(fmt.Sprintf("    \"%s\" %d;\n", policy.ID, policy.BanDurationSeconds))
	}
	result.WriteString("}\n\nlog_format cdn_security_json escape=json '{\"timestamp\":\"$time_iso8601\",\"policy_id\":\"$cdn_security_policy_id\",\"action\":\"$cdn_security_action\",\"ban_seconds\":$cdn_security_ban_seconds,\"client_ip\":\"$remote_addr\",\"host\":\"$host\",\"method\":\"$request_method\",\"path\":\"$uri\"}';\n")
	return result.String()
}

func renderRateLimitConfig(policies []domain.RateLimitPolicy, httpEnabled bool) string {
	if policies == nil {
		return ""
	}
	var result strings.Builder
	result.WriteString(RateLimitRevisionMarker(policies))
	result.WriteByte('\n')
	policies = enabledRateLimitPolicies(policies)
	if len(policies) == 0 || !httpEnabled {
		return result.String()
	}
	result.WriteString(`lua_shared_dict cdn_rate_limit 20m;
init_by_lua_block {
    local policies = {
`)
	for _, policy := range policies {
		encodedID, _ := json.Marshal(policy.ID)
		result.WriteString("        { id = ")
		result.Write(encodedID)
		result.WriteString(fmt.Sprintf(", limit = %d", policy.RequestsPerSecond))
		if policy.ResponseConditionEnabled {
			result.WriteString(", statuses = { ")
			for index, class := range policy.ResponseStatusClasses {
				if index > 0 {
					result.WriteString(", ")
				}
				result.WriteString(fmt.Sprintf("[%d] = true", class))
			}
			result.WriteString(" }")
		}
		result.WriteString(" },\n")
	}
	result.WriteString(`    }
    local key_ttl = 3

    local function bucket_key(policy, client_ip, second)
        return policy.id .. ":" .. client_ip .. ":" .. second
    end

    local function window_rate(dict, policy, client_ip, now, increment)
        local second = math.floor(now)
        local current_key = bucket_key(policy, client_ip, second)
        local current
        if increment then
            current = dict:incr(current_key, 1, 0, key_ttl)
            if current == nil then
                return nil
            end
        else
            current = dict:get(current_key) or 0
        end
        local previous = dict:get(bucket_key(policy, client_ip, second - 1)) or 0
        return current + previous * (1 - (now - second))
    end

    local function reject()
        ngx.header["Retry-After"] = "1"
        ngx.header["Cache-Control"] = "no-store"
        return ngx.exit(429)
    end

    local function access()
        if ngx.is_subrequest then
            return
        end
        local client_ip = ngx.var.remote_addr
        if client_ip == nil or client_ip == "" then
            return
        end
        local dict = ngx.shared.cdn_rate_limit
        local now = ngx.now()
        for _, policy in ipairs(policies) do
            local count_requests = policy.statuses == nil
            local rate = window_rate(dict, policy, client_ip, now, count_requests)
            if rate ~= nil and ((count_requests and rate > policy.limit) or
                (not count_requests and rate >= policy.limit)) then
                return reject()
            end
        end
    end

    local function response()
        if ngx.is_subrequest then
            return
        end
        local client_ip = ngx.var.remote_addr
        if client_ip == nil or client_ip == "" then
            return
        end
        local status_class = math.floor((tonumber(ngx.status) or 0) / 100)
        local dict = ngx.shared.cdn_rate_limit
        local second = math.floor(ngx.now())
        for _, policy in ipairs(policies) do
            if policy.statuses ~= nil and policy.statuses[status_class] then
                dict:incr(bucket_key(policy, client_ip, second), 1, 0, key_ttl)
            end
        end
    end

    package.loaded.cdn_platform_rate_limit = { access = access, response = response }
}
`)
	return result.String()
}

func SecurityRevisionMarker(policies []domain.SecurityPolicy) string {
	type revisionPolicy struct {
		ID                 string
		Name               string
		Enabled            bool
		Pattern            string
		Action             domain.SecurityPolicyAction
		BanDurationSeconds int
		Priority           int
	}
	ordered := append([]domain.SecurityPolicy(nil), policies...)
	sort.Slice(ordered, func(i, j int) bool { return ordered[i].ID < ordered[j].ID })
	revision := make([]revisionPolicy, 0, len(ordered))
	for _, policy := range ordered {
		revision = append(revision, revisionPolicy{
			ID: policy.ID, Name: policy.Name, Enabled: policy.Enabled, Pattern: policy.Pattern,
			Action: policy.Action, BanDurationSeconds: policy.BanDurationSeconds, Priority: policy.Priority,
		})
	}
	encoded, _ := json.Marshal(revision)
	digest := sha256.Sum256(encoded)
	return fmt.Sprintf("# CDN security revision: %x", digest[:])
}

func HasSecurityRevision(configuration string, policies []domain.SecurityPolicy) bool {
	return strings.Contains(configuration, SecurityRevisionMarker(policies))
}

func RateLimitRevisionMarker(policies []domain.RateLimitPolicy) string {
	type revisionPolicy struct {
		ID                       string
		Name                     string
		Enabled                  bool
		Key                      string
		RequestsPerSecond        int
		ResponseConditionEnabled bool
		ResponseStatusClasses    []int
	}
	ordered := append([]domain.RateLimitPolicy(nil), policies...)
	sort.Slice(ordered, func(i, j int) bool { return ordered[i].ID < ordered[j].ID })
	revision := make([]revisionPolicy, 0, len(ordered))
	for _, policy := range ordered {
		if normalized, err := domain.NormalizeRateLimitPolicy(policy); err == nil {
			policy = normalized
		}
		revision = append(revision, revisionPolicy{
			ID: policy.ID, Name: policy.Name, Enabled: policy.Enabled, Key: domain.RateLimitKeyClientIP,
			RequestsPerSecond:        policy.RequestsPerSecond,
			ResponseConditionEnabled: policy.ResponseConditionEnabled,
			ResponseStatusClasses:    append([]int(nil), policy.ResponseStatusClasses...),
		})
	}
	encoded, _ := json.Marshal(revision)
	digest := sha256.Sum256(encoded)
	return fmt.Sprintf("# CDN rate limit revision: %x", digest[:])
}

func HasRateLimitRevision(configuration string, policies []domain.RateLimitPolicy) bool {
	return strings.Contains(configuration, RateLimitRevisionMarker(policies))
}

func escapeNginxQuotedPattern(pattern string) string {
	pattern = strings.ReplaceAll(pattern, `\`, `\\`)
	return strings.ReplaceAll(pattern, `"`, `\"`)
}

func SiteHealthBody(siteID string) string {
	return "site=" + siteID
}

func HasSiteHealth(configuration, siteID string) bool {
	return strings.Contains(configuration, `return 200 "`+SiteHealthBody(siteID)+`\n";`)
}

func parseOrigin(origin domain.Origin) (*url.URL, error) {
	parsed, err := url.Parse(origin.URL)
	if err != nil || parsed.Scheme == "" || parsed.Host == "" {
		return nil, fmt.Errorf("must be an absolute URL")
	}
	if !domain.ValidOriginScheme(parsed.Scheme) {
		return nil, fmt.Errorf("scheme must be http, https, ws, wss, grpc, or grpcs")
	}
	if parsed.Path != "" && parsed.Path != "/" {
		return nil, fmt.Errorf("origin URL cannot include a path")
	}
	if parsed.Port() == "" {
		if domain.OriginUsesTLS(parsed.Scheme) {
			parsed.Host += ":443"
		} else {
			parsed.Host += ":80"
		}
	}
	return parsed, nil
}
